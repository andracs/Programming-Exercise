package Chapters.Chapter3;

public class Wife {
    double weight;
    boolean isPretty;
    boolean isCrazy;
    boolean goodCook;
    int age;
    String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public boolean isPretty() {
        return isPretty;
    }

    public void setPretty(boolean pretty) {
        isPretty = pretty;
    }

    public boolean isCrazy() {
        return isCrazy;
    }

    public void setCrazy(boolean crazy) {
        isCrazy = crazy;
    }

    public boolean isGoodCook() {
        return goodCook;
    }

    public void setGoodCook(boolean goodCook) {
        this.goodCook = goodCook;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
